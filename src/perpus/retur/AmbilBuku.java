    private void loadData(String where) {
        initTable();
        Connection kon = Koneksi.konek();
        Statement st;
        ResultSet rs;
        try {
            st = (Statement) kon.createStatement();
            String query = "SELECT * FROM BUKU "
                    + "JOIN SUPPLIER ON SUPPLIER.ID_SUPPLIER=BUKU.ID_SUPPLIER"
                    +where+" ORDER BY JUDUL ASC";
            rs = st.executeQuery(query);
            while(rs.next()){
                model.addRow(new Object[]{rs.getObject(1), rs.getObject(2), 
                    rs.getObject(9)});
            }
            rs.close();
            st.close();
            kon.close();
            tbBuku.setModel(model);
        } catch (SQLException e) {
        }
    }
