    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        char c = evt.getKeyChar();
        if(!((Character.isDigit(c) || Character.isLetter(c) || 
                c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE ||
                c == KeyEvent.VK_TAB || c == KeyEvent.VK_ENTER)))
        {
            evt.consume();
            JOptionPane.showMessageDialog(this, "Hanya dibolehkan huruf dan angka");
        } else{
            String key = txtCari.getText();
            if(key.length() >= 3){
                try {
                    String where = " WHERE ID_BUKU LIKE '%"+key+"%' OR JUDUL LIKE '%"
                            +key+"%' OR PENGARANG LIKE '"+key+"%' "
                            + "OR PENERBIT LIKE '"+key+"' OR ISBN LIKE '%"+key+"%' "
                            + "OR TAHUN=STR_TO_DATE('"+key+"', '%Y') "
                            + "OR BUKU.ID_SUPPLIER LIKE '%"+key+"%'"
                            + "OR SUPPLIER.NAMA_SUPPLIER LIKE '%"+key+"%'";
                    loadData(where);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error : Gagal Cari Data! "
                            + "Silahkan coba lagi!");
                }
            }
        }
    }//GEN-LAST:event_txtCariKeyReleased

