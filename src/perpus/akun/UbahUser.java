    private void btnSimpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpan1ActionPerformed
        String id = txtID.getText().toUpperCase();
        String username = txtUsername.getText().toUpperCase();
        String nama = txtNama.getText().toUpperCase();
        String role = cbRole.getSelectedItem().toString();

        if(id.isEmpty()){
            JOptionPane.showMessageDialog(this, "Kolom id masih kosong");
            txtUsername.requestFocus();
        } else if(username.isEmpty()){
            JOptionPane.showMessageDialog(this, "Kolom username masih kosong");
            txtUsername.requestFocus();
        } else if(nama.isEmpty()){
            JOptionPane.showMessageDialog(this, "Kolom nama masih kosong");
            txtNama.requestFocus();
        } else if(role.isEmpty()){
            JOptionPane.showMessageDialog(this, "Kolom role masih kosong");
            cbRole.requestFocus();
        }else{
            try {
                String query = "UPDATE USERS SET USERNAME=?, NAMA_USER=?, "
                        + "ROLE=? WHERE ID_USER=?";
                PreparedStatement ps = Koneksi.konek().prepareStatement(query);
                ps.setString(1, username);
                ps.setString(2, nama);
                ps.setString(3, role);
                ps.setString(4, id);

                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Berhasil perbarui data!");
                Users.refreshMethod();
                setVisible(false);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error : "+e.getMessage());
            }

        }
    }//GEN-LAST:event_btnSimpan1ActionPerformed

