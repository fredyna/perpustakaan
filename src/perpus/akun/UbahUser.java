    public void fillForm(){
        if(!id_user.isEmpty()){
            Connection kon = Koneksi.konek();
            try {
                Statement st = (Statement) kon.createStatement();
                String query = "SELECT * FROM USERS WHERE ID_USER='"+id_user+"'";
                ResultSet rs = st.executeQuery(query);
                DateFormat dateFormat = new SimpleDateFormat("yyyy");
                while(rs.next()){
                    txtID.setText(rs.getObject(1).toString());
                    txtUsername.setText(rs.getObject(2).toString());
                    txtNama.setText(rs.getObject(3).toString());
                    cbRole.setSelectedItem(rs.getObject(5).toString());
                }
                rs.close();
                st.close();
                kon.close();
            } catch (SQLException e) {
            }
        } else{
            JOptionPane.showMessageDialog(this, "ID buku tidak ada");
        }
    }
    

